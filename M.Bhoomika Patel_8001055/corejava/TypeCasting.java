class TypeCasting {
    public static void main(String[] args) {

        double num1 = 9.78;

        // Double to int
        int result1 = (int) num1;

        int num2 = 25;

        // Int to double
        double result2 = (double) num2;

        System.out.println("Double value: " + num1);
        System.out.println("Converted to int: " + result1);

        System.out.println("Int value: " + num2);
        System.out.println("Converted to double: " + result2);
    }
}